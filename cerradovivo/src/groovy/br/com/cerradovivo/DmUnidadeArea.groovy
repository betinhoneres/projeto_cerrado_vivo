	ha('hectare'),
	a('are'),
	ca('centiare'),
	km2('kilometro quadrado'),
	m2('metro quadrado');

	private final String toString

	DmUnidadeArea(String toString) {
		this.toString = toString
	}

	String getName() {
		name()
	}

	String toString() {
		toString
	}
}