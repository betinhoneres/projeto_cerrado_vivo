package br.com.cerradovivo;

public enum DmEstadoPreservacao {

	PRESERVADOISOLADO('Preservado e isolado'),
	PRESERVADONAOISOLADO('Preservado e não isolado'),
	AUSENCIAVEGETACAO('Ausência de vegetação'),
	PRESENCAANIMAIS('Presença de animais'),
	NAOISOLADO('Não está isolada');

	private final String toString

	DmEstadoPreservacao(String toString) {
		this.toString = toString
	}

	String getName() {
		name()
	}

	String toString() {
		toString
	}
}